                                    In 1898, Fr. Marcus Dombrowski, a Salvatorian missionary, arrived in Smit. He became friends with the local king, Khlur Sing, and, with permission, stayed at the Dak Bungalow in Smit. Later, he moved to Kut and then Lamin in 1901, where he formed the first Catholic community of 19 baptized people.The Salesians arrived in Shillong in 1922 and began visiting Smit and nearby villages. From 1924, Fr. Constantine Vendrame visited these villages with great missionary zeal. In 1928, on Christmas night, Fr. Vendrame baptized 11 people from Smit at the Laitumkhrah Church in Shillong. More baptisms followed in Shillong over the next few months. In 1929, with the king's permission, Fr. Vendrame built a small chapel at Smit, where St. Peter’s School now stands. However, the chapel was later dismantled due to lack of use.In the following decades, the Catholic community in Smit grew slowly. In the early 1980s, Fr. Michael Balawan began staying at St. Peter’s and actively engaged in missionary work. He was joined by Fr. NT Jose for some time. Fr. Sylvanus Sngi and brothers from Sacred Heart Theological College frequently visited on Sundays, helping the community grow. In 1984, the Salesian Province of Guwahati bought land for further missionary work. Fr. Balawan began building a new chapel in 1988. He passed away on 11 October 1989, and the chapel was blessed in 1990.Fr. Francis Kottekarotte, Parish Priest of Nongthymmai (1999-2004), built an additional school building at Smit, introducing the secondary section in 2001. Plans for a Don Bosco Centre at Smit were developed, with an agreement that the Salesians would oversee the new Laitkor Parish and eventually hand it over to the diocese, recognizing Smit as a Salesian Parish. Fr. Edmund Gomes was tasked with developing the Don Bosco Centre and began the work in Laitkor. On 24 May 2009, after completing the residence for the confreres, Fr. Gomes moved to Smit, continuing the construction of the school building. The school received government recognition on 3 August 2009.The number of Catholics in Smit and nearby villages continues to grow. From May to December 2009, Fr. Edmund Lakra served as Assistant at Smit. Fr. Anugrah Kerketta, ordained on 9 January 2010, joined Fr. Gomes on 20 January 2010. The school was upgraded to Higher Secondary (Arts) in 2014, and a Commerce stream was introduced in 2024.
